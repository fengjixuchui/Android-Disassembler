    private static final Map<Integer, UddTag> intToTypeMap = new HashMap<Integer, UddTag>();

    static {
        for (UddTag tag : UddTag.values()) {
            intToTypeMap.put(tag.numVal, tag);
        }
    }

    int numVal;

    UddTag(int numVal) {
        this.numVal = numVal;
    }

    public static UddTag fromInt(int i) {
        UddTag tag = intToTypeMap.get(Integer.valueOf(i));
        if (tag == null)
            return UddTag.TAG_UNKNOWN;
        return tag;
    }

    public int getNumVal() {
        return numVal;
    }
